                    <h1>水性漆该如何选择？看完就是专家</h1>
                    <div class="info_con_tit">
                        <div class="info_con_tit">
                            <div class="n_tit"> <span class="fr" style="margin:0px;"><a href="/hyxw/" title="返回列表" target="_blank"><img src="/spectacle/images/article_btn.gif" alt="返回列表"></a></span> <span>来源：水性漆</span>                  </span> <span>浏览：<span id="cntrHits">1949</span></span> <span>发布日期：2019-09-12【<a href="javascript:;" onclick="ChangeFontSize(this,'16px')">大</a> <a class="cur" href="javascript:;" onclick="ChangeFontSize(this,'14px')">中</a> <a href="javascript:;" onclick="ChangeFontSize(this,'12px')">小</a>】</span> </div>
                        </div>
                    </div>
                    <div id="cntrBody"> <p style="text-indent:2em;">
                            <span style="font-family: font-size:14px;color:#666666;">
                                <span style="font-family: Microsoft YaHei; font-size: 14px; color: rgb(102, 102, 102);"></span>
                                <span style="font-family: Microsoft YaHei; font-size: 14px; color: rgb(102, 102, 102);">
                                    一、要清楚是用什么树脂制造的
                                    <a href="http://www.dhq898.com" target="_blank">水性漆</a>
                                </span>
                            </span>
                        </p>
                        <p style="text-indent:2em;">
                            <span style="font-family: Microsoft YaHei; font-size: 14px; color: rgb(102, 102, 102);">水性树脂分类非常清晰，各类树脂性能、功能和价格不一或相差甚大，直接影响水性漆的品质、性能、效果和成本，一定要选择能满足自己需求的产品。</span>
                        </p>
                        <div style="text-align:center;">
                            <img src="/Uploads/Picture/20190912/5d7a1418b0c37.jpg" alt="水性漆该如何选择？看完就是专家_水性油漆|大荷水性漆" width="500" height="367" title="水性漆该如何选择？看完就是专家_水性油漆|大荷水性漆" align style="text-indent:2em;" />
                        </div>
                        <p style="text-indent:2em;">
                            <span style="font-family: Microsoft YaHei; font-size: 14px; color: rgb(102, 102, 102);">二、对水性漆的产品进行最基本的检查</span>
                        </p>
                        <p style="text-indent:2em;">
                            <span style="font-family: Microsoft YaHei; font-size: 14px; color: rgb(102, 102, 102);">1）味道：不能有强烈的刺鼻味道，不能有较强的酒精气味，更不能有强烈的异味，因为味道都是由添加剂决定的，要提防弄虚作假和技术不过关的水性漆。</span>
                        </p>
                        <div style="text-align:center;">
                            <img src="/Uploads/Picture/20190912/5d7a1418e34b5.png" alt="水性漆该如何选择？看完就是专家_水性油漆|大荷水性漆" width="500" height="400" title="水性漆该如何选择？看完就是专家_水性油漆|大荷水性漆" align style="text-indent:2em;" />
                        </div>
                        <p style="text-indent:2em;">
                            <span style="font-family: Microsoft YaHei; font-size: 14px; color: rgb(102, 102, 102);">2）乳液性状：一般是透明至乳白色，不要拿稠不稠(即粘度)来判断产品好坏，毕竟稠不稠是由成本较低的增稠剂来决定，关键是色泽和粘度样品要吻合。</span>
                        </p>
                        <div style="text-align:center;">
                            <img src="/Uploads/Picture/20190912/5d7a141905b3c.jpg" alt="水性漆该如何选择？看完就是专家_水性油漆|大荷水性漆" width="500" height="333" title="水性漆该如何选择？看完就是专家_水性油漆|大荷水性漆" align style="text-indent:2em;" />
                        </div>
                        <p style="text-indent:2em;">
                            <span style="font-family: Microsoft YaHei; font-size: 14px; color: rgb(102, 102, 102);">3）分散性:直观看包装物内产品乳液，不沉淀、分散均匀为上品，这样可以减少使用难度或避免产品变质，除个别特殊设计的产品以外。</span>
                        </p>
                        <div style="text-align:center;">
                            <img src="/Uploads/Picture/20190912/5d7a141930efd.png" alt="水性漆该如何选择？看完就是专家_水性油漆|大荷水性漆" width="500" height="365" title="水性漆该如何选择？看完就是专家_水性油漆|大荷水性漆" align style="text-indent:2em;" />
                        </div>
                        <p style="text-indent:2em;">
                            <span style="font-family: Microsoft YaHei; font-size: 14px; color: rgb(102, 102, 102);">三、对水性漆及其漆膜性能进行测试和检査</span>
                        </p>
                        <p style="text-indent:2em;">
                            <span style="font-family: Microsoft YaHei; font-size: 14px; color: rgb(102, 102, 102);">1）打磨性:干砂纸打磨要求起粉和不粘砂纸，否则，砂纸耗用量会大量增加，并且打磨出的漆膜平整度会下降，影响涂装品质。</span>
                        </p>
                        <div style="text-align:center;">
                            <img src="/Uploads/Picture/20190912/5d7a141944327.jpg" alt="水性漆该如何选择？看完就是专家_水性油漆|大荷水性漆" width="500" height="257" title="水性漆该如何选择？看完就是专家_水性油漆|大荷水性漆" align style="text-indent:2em;" />
                        </div>
                        <p style="text-indent:2em;">
                            <span style="font-family: Microsoft YaHei; font-size: 14px; color: rgb(102, 102, 102);">2）耐黄变:涂装在玻璃片或白瓷片上，待干后，在太阳底下暴晒3天以上或在紫外线灯下照射4个小时以上，观察漆膜的黄变情形，黄变越小就越好，不黄变就最好。</span>
                        </p>
                        <div style="text-align:center;">
                            <img src="/Uploads/Picture/20190912/5d7a14196987d.jpg" alt="水性漆该如何选择？看完就是专家_水性油漆|大荷水性漆" width="500" height="334" title="水性漆该如何选择？看完就是专家_水性油漆|大荷水性漆" align style="text-indent:2em;" />
                        </div>
                        <p style="text-indent:2em;">
                            <span style="font-family: Microsoft YaHei; font-size: 14px; color: rgb(102, 102, 102);">3）表干时间:温度≥5℃、湿度≤80%，空气正常流通条件下，表干时间≤10分钟，否则容易出现粘灰尘、干燥慢就难打磨、重涂时间加长和延长作业流程等弊端，表干时间越长越难以应用。</span>
                        </p>
                        <div style="text-align:center;">
                            <img src="/Uploads/Picture/20190912/5d7a141aa046d.jpg" alt="水性漆该如何选择？看完就是专家_水性油漆|大荷水性漆" width="500" height="268" title="水性漆该如何选择？看完就是专家_水性油漆|大荷水性漆" align style="text-indent:2em;" />
                        </div>
                        <p style="text-indent:2em;">
                            <span style="font-family: Microsoft YaHei; font-size: 14px; color: rgb(102, 102, 102);">4）附着力:涂装完成待干7天后，用美工刀片和直尺划出10x10格、约1mmx1mm的小格，用透明胶带贴稳后，用力急上拉，粘掉3格以内合格，最好是一个不掉，当然，最标准的方法是用百格刀和3M不干胶予以测试。</span>
                        </p> </div>
